package hekai.model;

public class Grade {
    private String Sno;
    private String Con;
    private String grade;

    public String getSno() {
        return Sno;
    }

    public void setSno(String sno) {
        Sno = sno;
    }

    public String getCon() {
        return Con;
    }

    public void setCon(String con) {
        Con = con;
    }

    public String getGrade() {
        return grade;
    }

    public void setGrade(String grade) {
        this.grade = grade;
    }
}
